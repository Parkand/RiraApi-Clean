using System;

namespace Rira.Domain.Enums
{
    // 🧩 Enum دامنه‌ای TaskStatus
    // ===============================================================================
    // این Enumeration بیانگر وضعیت‌های ممکن برای یک "تسک" (وظیفه کاری)
    // در سیستم ریرا است و مستقیماً در Entity، DTO و Commandهای مربوط به Task
    // مورد استفاده قرار می‌گیرد.
    //
    // 🎯 هدف طراحی:
    //     ▫ تعریف وضعیت‌های استاندارد چرخه‌ی عمر تسک.
    //     ▫ جلوگیری از استفاده از رشته‌ها یا اعداد نامشخص در کد (Magic Numbers).
    //     ▫ پشتیبانی از نگاشت مستقیم بین Domain و Application (AutoMapper).
    //
    // 🔹 نکته RiRaDocs:
    //     این Enum در فضای نام Rira.Domain.Enums تعریف شده تا لایه‌ی Domain
    //     مستقل از EF و Presentation باقی بماند — یعنی وضعیت‌ها در هسته‌ی دامنه
    //     تعریف شوند و تغییرات رابط کاربری تأثیری بر منطق کسب‌وکار نداشته باشند.
    public enum TaskStatus
    {
        // -----------------------------------------------------------------------
        // 🔸 وضعیت ۱ — Pending
        // -----------------------------------------------------------------------
        // «در انتظار شروع»: یعنی تسک ثبت شده ولی هنوز آغاز نشده است.
        // معمولاً اولین وضعیت پس از ایجاد TaskCreateCommand می‌باشد.
        Pending = 1,

        // -----------------------------------------------------------------------
        // 🔸 وضعیت ۲ — InProgress
        // -----------------------------------------------------------------------
        // «در حال انجام»: تسک توسط مسئول مربوطه شروع شده و هنوز تکمیل نشده است.
        // در UI معمولاً با رنگ آبی نمایش داده می‌شود.
        InProgress = 2,

        // -----------------------------------------------------------------------
        // 🔸 وضعیت ۳ — Completed
        // -----------------------------------------------------------------------
        // «تکمیل شده»: عملیات با موفقیت تمام شده و دیگر نیازی به تغییر ندارد.
        // در Reportها، این وضعیت اغلب در محاسبه‌ی بهره‌وری استفاده می‌شود.
        Completed = 3,

        // -----------------------------------------------------------------------
        // 🔸 وضعیت ۴ — Cancelled
        // -----------------------------------------------------------------------
        // «لغو شده»: تسک به هر دلیل (فنی یا مدیریتی) لغو شده است.
        // در حذف نرم نیز معمولاً وضعیت تغییر نمی‌کند تا تاریخچه حفظ شود.
        Cancelled = 4
    }

    // ===============================================================================
    // 📘 خلاصه آموزشی RiRaDocs Teaching Edition
    // ------------------------------------------------------------------------------
    // 🔹 مفهوم دامنه‌ای:
    //     TaskStatus یک Enum دامنه‌ای (Domain Enum) برای مدیریت وضعیت‌های تسک‌هاست.
    //     این Enum چرخه‌ی کامل حیات یک تسک را از ایجاد تا پایان نمایش می‌دهد.
    //
    // 🔹 ارتباط لایه‌ها:
    //     ▫ Domain → تعریف ارزش‌های Enum
    //     ▫ Application → استفاده در Commandها (مانند TaskCreateCommand و TaskUpdateCommand)
    //     ▫ DTO → نمایش در خروجی API در قالب رشته‌ی خوانا
    //
    // 🔹 اصول طراحی رعایت‌شده:
    //     ▫ Domain Driven Enum — تعریف وضعیت‌ها در مرکز دامنه.
    //     ▫ Type Safety — جلوگیری از خطای ورودی مقادیر نادرست.
    //     ▫ Layer Consistency — یکسان‌سازی بین همه‌ی لایه‌ها.
    //
    // 🔹 وابستگی مستقیم:
    //     ▫ TaskEntity → فیلد Status
    //     ▫ TaskDto / TaskCreateDto / TaskUpdateDto → فیلد Status
    //     ▫ MappingProfiles → نگاشت Enumها به مدل‌های نمایشی
    //
    // 🔹 تگ انتشار RiRaDocs:
    //     RiraDocs-v2025.11.4-Stable-Final-Fixed
    // ===============================================================================
}
