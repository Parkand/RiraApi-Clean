namespace Rira.Domain.Enums
{
    // 🧩 Enum دامنه‌ای TaskPriority
    // ===============================================================================
    // این Enum بیانگر سطوح مختلف اولویت (Priority) برای تسک‌ها در سیستم ریرا است.
    // از آن در تمام لایه‌های Domain, Application و Presentation برای نمایش یا ذخیره‌ی
    // میزان اهمیت وظایف استفاده می‌شود.
    //
    // 🎯 کاربرد اصلی:
    //     ▫ مشخص‌کردن مقدار استاندارد در فیلد Priority از کلاس TaskEntity.
    //     ▫ جلوگیری از ثبت مقادیر متفرقه (Magic Numbers) در پایگاه داده.
    //     ▫ بهبود خوانایی کد در Commands و DTOها (مانند TaskCreateCommand یا TaskDto).
    //
    // 🔹 نکته RiRaDocs:
    //     برخلاف رشته‌های متنی در UI، Enumها در Domain
    //     به‌صورت مقدار عددی (int) ذخیره می‌شوند و در DTO با ToString() نمایش داده می‌شوند.
    public enum TaskPriority
    {
        // -----------------------------------------------------------------------
        // 🔸 سطح ۱ — پایین‌ترین سطح اولویت
        // -----------------------------------------------------------------------
        // زمان اجرای تسک با کمترین فوریت. مناسب برای کارهای ثانویه یا غیراصلی.
        Low = 1,

        // -----------------------------------------------------------------------
        // 🔸 سطح ۲ — اولویت متوسط
        // -----------------------------------------------------------------------
        // دارای اهمیت متوسط؛ نیازمند انجام در زمان مشخص اما نه فوری.
        Medium = 2,

        // -----------------------------------------------------------------------
        // 🔸 سطح ۳ — اولویت بالا
        // -----------------------------------------------------------------------
        // موارد کلیدی و مهم که باید در صف سریع‌تر اجرا شوند.
        High = 3,

        // -----------------------------------------------------------------------
        // 🔸 سطح ۴ — اولویت بحرانی
        // -----------------------------------------------------------------------
        // نشان‌دهنده تسک‌های بحرانی (Emergency) که نیاز به اقدام فوری دارند.
        // معمولاً توسط مدیر پروژه تعیین می‌شوند و در UI با رنگ خاص نمایش داده می‌شوند.
        Critical = 4
    }

    // ===============================================================================
    // 📘 خلاصه آموزشی RiRaDocs Teaching Edition
    // ------------------------------------------------------------------------------
    // 🔹 مفهوم دامنه‌ای:
    //     TaskPriority یک Enumeration دامنه‌ای (Domain Enum) است که نقش
    //     «کلاسه‌بندی اهمیت وظایف» را ایفا می‌کند تا تمام بخش‌های پروژه
    //     (مانند Command، Query، DTO و Entity) از تعریف یکنواخت استفاده کنند.
    //
    // 🔹 ارتباط با دیگر کلاس‌ها:
    //     ▫ TaskEntity → فیلد Priority از همین Enum استفاده می‌کند.
    //     ▫ TaskDto / TaskCreateCommand / TaskUpdateCommand → از نوع TaskPriority هستند.
    //     ▫ MappingProfile → عملیات نگاشت بین Domain و Application را برای این Enum انجام می‌دهد.
    //
    // 🔹 اصول طراحی رعایت‌شده:
    //     ▫ Domain Driven Enumeration — تعریف مقادیر معنایی الزام‌آور در هسته دامنه.
    //     ▫ Consistency Across Layers — یکسان‌سازی معنای Priority در تمام لایه‌ها.
    //     ▫ Type Safety — کاهش احتمال خطا در استفاده از اعداد یا رشته‌های پراکنده.
    //
    // 🔹 تگ انتشار:
    //     RiraDocs-v2025.11.4-Stable-Final-Fixed
    // ===============================================================================
}
