using MediatR;
using Rira.Application.Common;
using Rira.Application.DTOs;
using System.Collections.Generic;

namespace Rira.Application.Features.Tasks.Queries.GetAll
{
    // 🧩 کلاس کوئری دریافت همه‌ی تسک‌ها — TaskGetAllQuery
    // -------------------------------------------------------------------
    // این کلاس بخش "Query" از الگوی CQRS را برای ماژول Task پیاده‌سازی می‌کند.
    // در CQRS، Queryها مخصوص عملیات خواندن (Read Operations) هستند 
    // و هیچ تغییری در حالت سیستم ایجاد نمی‌کنند.
    //
    // 📌 نقش این کلاس:
    //     ▫ فقط تعریف درخواست (Request Definition)
    //     ▫ بدون منطق پردازش یا دسترسی به پایگاه داده
    //
    // ⚙️ جریان اجرای Query:
    //     1️⃣ Controller متد GetAll را صدا می‌زند.
    //     2️⃣ MediatR یک نمونه از TaskGetAllQuery می‌فرستد.
    //     3️⃣ هندلر مربوطه (TaskGetAllQueryHandler) این Query را دریافت و اجرا می‌کند.
    //     4️⃣ نتیجه در قالب ResponseModel<List<TaskDto>> بازگردانده می‌شود.
    //
    // 🧠 در RiRa، برای هر Query یک Handler متناظر با نامی مشابه وجود دارد:
    //     TaskGetAllQuery  ⇢  TaskGetAllQueryHandler
    // -------------------------------------------------------------------
    public class TaskGetAllQuery : IRequest<ResponseModel<List<TaskDto>>>
    {
        // 🔸 این Query نیازی به پارامتر ورودی ندارد،
        // زیرا هدف آن دریافت فهرست کامل تسک‌ها از سیستم است.
        //
        // 🔸 نوع بازگشتی:
        // ResponseModel<List<TaskDto>> 
        // یعنی یک مدل پاسخ استاندارد که شامل لیست DTOهای تسک است
        // همراه با وضعیت، پیام و کد HTTP شبیه سایر درخواست‌ها.
    }

    // ===========================================================================================
    // 📘 خلاصه آموزشی (RiraDocs Teaching Edition)
    // -------------------------------------------------------------------------------------------
    // 🔹 مسئولیت کلاس:
    //     ▫ تعریف یک Query برای "خواندن" داده‌ها از سیستم (تمام تسک‌ها).
    //     ▫ نداشتن هیچ منطق بیزنسی یا دیتابیس (فقط تعریف Request).
    //
    // 🔹 نوع بازگشت:
    //     ▫ ResponseModel<List<TaskDto>> 
    //       شامل:
    //         - Success (نتیجه اجرا)
    //         - Message (توضیح وضعیت)
    //         - StatusCode (کد وضعیت HTTP)
    //         - Data (فهرست TaskDtoها)
    //
    // 🔹 ارتباط معماری:
    //     ▫ مربوط به بخش Query در CQRS.
    //     ▫ توسط MediatR به Handler مربوطه ارسال می‌شود.
    //
    // 🔹 نکته طراحی:
    //     ▫ طراحی مینیمال و تمیز (Clean Definition) برای Queryها.
    //     ▫ در پروژه RiRa، نام Queryها با ساختار {Feature}{Action}Query تنظیم شده.
    //
    // 🔹 تست‌پذیری:
    //     ▫ کلاس Query به‌تنهایی تست خاصی ندارد (فقط داده‌بر است)، 
    //       اما در تست هندلر از آن به‌عنوان ورودی استفاده می‌شود.
    //
    // 🔹 بدون تغییر اجرایی در کد — فقط توضیحات آموزشی فارسی افزوده شده‌اند.
    // ===========================================================================================
}
