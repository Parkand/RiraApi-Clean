using MediatR;
using Rira.Application.Common;

namespace Rira.Application.Features.Employees.Commands.DeleteEmployee
{
    // 🗑 فرمان حذف کارمند از سیستم (Command Pattern)
    // ------------------------------------------------------------
    // این کلاس یکی از Commandهای CQRS است که فقط شناسهٔ کارمند مورد حذف را حمل می‌کند.
    // Handler مربوطه وظیفه دارد بر اساس این Id، رکورد را از دیتابیس حذف کند.
    // خروجی نوع ResponseModel<int> است؛ عدد مشخصهٔ حذف شده یا کد وضعیت برمی‌گردد.
    public class EmployeeDeleteCommand : IRequest<ResponseModel<int>>
    {
        // شناسه یکتای کارمند برای حذف از پایگاه داده
        public int Id { get; set; }

        // ===========================================================================================
        // 📘 خلاصه آموزشی (RiraDocs Teaching Edition)
        // -------------------------------------------------------------------------------------------
        // 🔹 این کلاس نمونه‌ای از Command ساده در معماری CQRS است.
        // 🔹 IRequest<ResponseModel<int>> نشان‌دهندهٔ خروجی استاندارد پاسخ‌دهی پروژه است.
        // 🔹 هیچ منطق تجاری در این Command وجود ندارد؛ فقط داده (Id) را منتقل می‌کند.
        // 🔹 حذف واقعی در Handler مربوطه انجام می‌شود، نه در خود Command.
        // 🔹 این ساختار باعث تفکیک وظایف (Separation of Concerns) و تمیزی معماری می‌گردد.
        // ===========================================================================================
    }
}
