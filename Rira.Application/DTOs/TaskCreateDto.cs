using System.ComponentModel.DataAnnotations;
using System;

namespace Rira.Application.DTOs
{
    // 🧩 DTO برای ایجاد یک Task جدید
    // ----------------------------------------------------
    // این کلاس داده‌هایی را که کاربر برای ساخت یک وظیفه (Task) ارسال می‌کند، مدل‌سازی می‌کند.
    // شامل فیلدهای اصلی موردنیاز برای ثبت اولیه‌ی وظیفه در سیستم است.
    // در معماری Clean Architecture، این مدل بین UI و لایه‌ی Application عمل می‌کند.
    public class TaskCreateDto
    {
        // عنوان وظیفه (الزامی)
        // [Required] باعث می‌شود هنگام ولیدیشن، نبود مقدار برای این فیلد خطا ایجاد کند.
        // [StringLength] طول مجاز عنوان را محدود می‌کند تا از ورود داده‌های طولانی یا نادرست جلوگیری شود.
        [Required(ErrorMessage = "عنوان (Title) الزامی است.")]
        [StringLength(100, ErrorMessage = "عنوان نمی‌تواند بیشتر از 100 کاراکتر باشد.")]
        public string Title { get; set; } = string.Empty;

        // توضیحات وظیفه (اختیاری ولی دارای محدودیت طول)
        [StringLength(500, ErrorMessage = "توضیحات (Description) نمی‌تواند بیشتر از 500 کاراکتر باشد.")]
        public string Description { get; set; } = string.Empty;

        // تاریخ سررسید وظیفه، مشخص‌کننده‌ی زمان پایان برنامه‌ریزی‌شده
        // این فیلد توسط کاربر وارد می‌شود و در دیتابیس ثبت خواهد شد.
        public DateTime DueDate { get; set; }

        // نکته:
        // فیلد IsCompleted در این DTO عمداً تعریف نشده چون هنگام ایجاد وظیفه،
        // وضعیت وظیفه باید پیش‌فرض "False" باشد تا توسط سیستم مدیریت شود.

        // ===========================================================================================
        // 📘 خلاصه آموزشی (RiraDocs Teaching Edition)
        // -------------------------------------------------------------------------------------------
        // این DTO برای عملیات ایجاد وظیفه (Create Task) در لایه Application طراحی شده است.
        // ویژگی‌های DataAnnotation مثل [Required] و [StringLength] باعث ولیدیشن خودکار می‌شوند
        // که در ترکیب با FluentValidation و ModelState کار می‌کنند.
        // وظیفه‌ی آن ایجاد مرز سالم میان داده‌ی ورودی کاربر و موجودیت‌های Domain است.
        // هیچ تغییر اجرایی انجام نشده — فقط توضیحات آموزشی برای تیم توسعه ریرا افزوده شده.
        // ===========================================================================================
    }
}
